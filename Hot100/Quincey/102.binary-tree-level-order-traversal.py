# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def levelOrder(self, root: Optional[TreeNode]) -> List[List[int]]:
        if not root:
            return []
        parent = [root]
        chld = []
        res = []
        while parent:
            temp = []
            for node in parent:
                if node.left:
                    chld.append(node.left)
                if node.right:
                    chld.append(node.right)
                if node:
                    temp.append(node.val)
            parent,chld = chld,[]
            res.append(temp)
        return res
        
